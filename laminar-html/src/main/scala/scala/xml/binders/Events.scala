package scala.xml
package binders

object Events {
  def key(e: String): Option[String] = unapply(e)

  def unapply(e: String): Option[String] = {
    if e.startsWith("on") then eventKey.find(key => key.equalsIgnoreCase(e.drop(2)))
    else None
  }

